/**
 * Supabase server client — use inside Server Components, Route Handlers,
 * and middleware. Reads / writes cookies via the Next.js cookies() API.
 */
import { createServerClient } from "@supabase/ssr";
import { cookies } from "next/headers";

export async function createClient() {
  const cookieStore = await cookies();

  return createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() {
          return cookieStore.getAll();
        },
        setAll(cookiesToSet) {
          try {
            for (const { name, value, options } of cookiesToSet) {
              cookieStore.set(name, value, options);
            }
          } catch {
            // setAll is called from Server Components where mutation is not allowed.
            // Middleware handles the actual cookie write; this is a no-op there.
          }
        },
      },
    },
  );
}
